                    <p>To illustrate this issue, we have made up a girl named Adrisana. She has just turned 12 years, and just got her first menstruation. She lives right outside of Luanda, the capital of Angola, Africa. At first, she does not understand what is happening. She thinks there is something seriously wrong with her. She bleeds, without knowing how to stop it. Her first thoughts are to find a room where she can fix this, like a toilet. But her school do not have real toilets, only pit latrines outside, where people can see her. She feels the shame just by the thought of sitting above the pit latrine and trying to clean herself.</p>
