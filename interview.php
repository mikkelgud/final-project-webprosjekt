            <img src="images/ellen.png" alt="Profile picture of Ellen Sadøe" />
            <p style="font-weight: bold;">Julie in "Orange Room" had an interview with Ellen Sandøe in early November 2019 about women`s sanitation in schools in developing countries. Ellen works with education, management and child rights daily, both in Norway and abroad. She is very positive to the "Orange Room» and would like to point out how important such work is for young girls around the globe.</p>
            <p>Julie: Tell us a little bit about the sanitary and menstrual conditions of young girls in developing countries.</p>
            <p>Ellen: The conditions are bare. The sanitary conditions for girls, especially in puberty, is a major obstacle for a lot of young girls. Girls even stop attending school because of this factor. The awareness around this issue has increased in the recent years, and today we work for separate toilets for boys and girls. Everyone should have the opportunity to wash their hands under running water as well. Sanitary conditions for girls are an important issue for UNICEF, especially regarding clean water. We have our own project called "Wash", which is based on this subject.</p>
                <p>Julie: UNICEF has already built separate girl toilets in schools in several countries, such as Malawi, Mongolia, India, Kenya, etc. Has this led to more stigmatization? Do you think separate rooms, or booths, made only for menstruation issues, would form even more stigmatization at schools around these subjects?</p>
               <p>Ellen: UNICEF`s experience indicates that this has only made the students more comfortable around the issues. The upgrading of the schools made the girls feel safer. When you feel safe, the insecurity which feeds the shame will go away. As a result, you will get less stigmatization. When I went to a school in Angola last year, the school showed off their new upgraded areas with huge pride. For them, it reflects a better quality all together, both for them, and for the school. Therefore, do not think the Orange Room would be a problem in the areas I visited, even if it is for only menstruation women.</p>
               <p>Julie: Can you explain how bad the conditions were at some of the schools you visited?</p>
            <p>Ellen: The reality is brutal. At its worst, there are no toilets at all. In some schools, there may only be a small room for both girls and boys, where there only is a hole in the floor. Some school has several toilets, where the toilets are not separated. No walls between them. There is shortage of good quality water systems. This results in a great lack of cleanliness. I have visited and seen this myself, and it makes an impact. The toilets (if you can call it a toilet) is a big source of infection, as it is very unhygienic. A big source of diseases for both children and youth.</p>
              <p>Julie: In "The Orange Room", we make safe and hygienic booths for menstruating youth in developing countries. Our goal is to increase education for young girls. What should one keep in mind when distributing this to schools around the world?</p>
              <p>Ellen: Something to keep in mind is that something that works in Angola, could be a completely wrong to do in a lot of places in Asia. There exists a lot of cultural differences to have in mind. But the solution of the orange room itself is very good, and we need new solutions with the way it all looks today.</p>
